/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     struct TreeNode *left;
 *     struct TreeNode *right;
 * };
 */



#define max(a,b) \
   ({ __typeof__ (a) _a = (a); \
       __typeof__ (b) _b = (b); \
     _a > _b ? _a : _b; })

int maxDepth(struct TreeNode* root){
    int maxlevel = -1;
    if ( !root ) return 0;
    if ( !root->left && !root->right ) return 1;
    if ( root->left ) maxlevel = max(maxlevel, maxDepth(root->left)+1);
    if ( root->right ) maxlevel = max(maxlevel, maxDepth(root->right)+1);
    return maxlevel;
}

